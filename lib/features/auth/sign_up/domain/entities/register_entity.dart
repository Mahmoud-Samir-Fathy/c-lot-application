class UserRegister {
  final String firstName;
  final String lastName;
  final String email;
  final String password;
  final String age;
  final String gender;
  final String address;

  UserRegister({
    required this.lastName,
    required this.email,
    required this.firstName,
    required this.password,
    required this.age,
    required this.gender,
    required this.address,
  });

}
